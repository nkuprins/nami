const photo = (seed: string, wide = false) =>
  wide
    ? `https://picsum.photos/seed/realestate-${seed}/1920/1080`
    : `https://picsum.photos/seed/realestate-${seed}/1200/900`;

const photos = (...seeds: string[]) => seeds.map((s) => photo(s));
const widePhotos = (...seeds: string[]) => seeds.map((s) => photo(s, true));

const plans = (...seeds: string[]) =>
  seeds.map((s) => `https://picsum.photos/seed/plan-${s}/1200/900`);

// Flat, human-writable shape for mock fixtures — converted to the nested
// ListingDetail shape by toListingDetail() below. Multiple raw entries can
// share a propertyId to model "one property, several listings".
interface RawListing {
  id: string;
  propertyId: string;
  type: ListingType;
  propertyKind: PropertyKind;
  titleLv?: string;
  titleEn?: string;
  titleRu?: string;
  descriptionLv?: string;
  descriptionEn?: string;
  descriptionRu?: string;
  price: number;
  vatIncluded?: boolean;
  rooms: number;
  bedrooms?: number;
  bathrooms?: number;
  bathroomLayout?: BathroomLayout;
  m2: number;
  landM2?: number;
  floor?: number;
  totalFloors?: number;
  yearBuilt?: number;
  heating?: HeatingType;
  energyClass?: EnergyClass;
  maintenanceCost?: number;
  sewage?: SewageType;
  ventilation?: VentilationType;
  roof?: RoofType;
  features: Feature[];
  ventilationSystems?: VentilationSystem[];
  communications?: Communication[];
  stove?: StoveType[];
  security?: SecurityFeature[];
  extras?: PropertyExtra[];
  parking?: ParkingType[];
  district: string;
  city: string;
  address: string;
  coords: { lat: number; lng: number };
  phones?: string[];
  photos: string[];
  plans?: string[];
  videoUrl?: string;
  websiteUrl?: string;
  postedAt: string;
  completion?: PropertyCompletion;
}

function toListingDetail(r: RawListing): ListingDetail {
  const translations: ListingDetail['translations'] = {};
  if (r.titleLv)
    translations.lv = { title: r.titleLv, description: r.descriptionLv };
  if (r.titleEn)
    translations.en = { title: r.titleEn, description: r.descriptionEn };
  if (r.titleRu)
    translations.ru = { title: r.titleRu, description: r.descriptionRu };

  return {
    id: r.id,
    propertyId: r.propertyId,
    type: r.type,
    propertyKind: r.propertyKind,
    price: { amount: r.price, vatIncluded: r.vatIncluded },
    details: {
      rooms: r.rooms,
      bedrooms: r.bedrooms,
      bathrooms: r.bathrooms,
      bathroomLayout: r.bathroomLayout,
      m2: r.m2,
      landM2: r.landM2,
      floor: r.floor,
      totalFloors: r.totalFloors,
      yearBuilt: r.yearBuilt,
      heating: r.heating,
      energyClass: r.energyClass,
      maintenanceCost: r.maintenanceCost,
      sewage: r.sewage,
      ventilation: r.ventilation,
      roof: r.roof,
    },
    translations,
    location: {
      district: r.district,
      city: r.city,
      address: r.address,
      coords: r.coords,
    },
    features: r.features,
    ventilationSystems: r.ventilationSystems ?? null,
    communications: r.communications ?? null,
    stove: r.stove ?? null,
    security: r.security ?? null,
    extras: r.extras ?? null,
    parking: r.parking ?? null,
    media: {
      photos: r.photos,
      plans: r.plans ?? null,
      videoUrl: r.videoUrl ?? null,
      websiteUrl: r.websiteUrl ?? null,
    },
    phones: r.phones?.map((phone) => ({ phone, name: '', email: '' })) ?? null,
    postedAt: r.postedAt,
    completion: r.completion,
    status: 'active',
  };
}

